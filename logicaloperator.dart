void main() {
  int a = 4;
  int b = 9;
  //&& AND
  if (a > 10 && b > 10) {
    print("false");
  } else {
    print("true");
  }
  // || OR

  if (a < 10 || b < 10) {
    print("false");
  } else {
    print("true");
  }
  //! NOT
  if (!(a > 10)) {
    print("false");
  } else {
    print("true");
  }
}
